const translatableNodes = document.querySelectorAll("[data-i18n]");
const languageButtons = document.querySelectorAll(".lang-button[data-language]");
const revealItems = document.querySelectorAll("[data-reveal]");
const floatingTopButton = document.querySelector(".floating-top-button");
const audienceSection = document.querySelector("#para-quem");
const siteHeader = document.querySelector(".site-header");
const contactForm = document.querySelector("[data-contact-form]");
const contactFormError = contactForm?.querySelector("[data-form-error]");
const contactFormSuccess = contactForm?.querySelector("[data-form-success]");
const contactFormSubmitButton = contactForm?.querySelector("[data-form-submit]");
const currentYearNodes = document.querySelectorAll("[data-current-year]");

const readStoredLanguage = () => {
  try {
    return window.localStorage.getItem("preferred-language");
  } catch {
    return null;
  }
};

const storeLanguage = (language) => {
  try {
    window.localStorage.setItem("preferred-language", language);
  } catch {
    // Ignore storage issues and keep the page functional.
  }
};

const getActiveDictionary = () => translations[document.body.dataset.language] || translations.pt;

const applyLanguage = (language) => {
  const dictionary = translations[language] || translations.pt;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    const attribute = node.dataset.i18nAttr;
    const value = dictionary[key];

    if (typeof value !== "string") return;

    if (attribute) {
      node.setAttribute(attribute, value);
      return;
    }

    node.textContent = value;
  });

  document.documentElement.lang = language === "en" ? "en" : "pt-BR";
  document.body.dataset.language = language;

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  if (contactFormError?.dataset.messageKey) {
    contactFormError.textContent =
      dictionary[contactFormError.dataset.messageKey] || contactFormError.textContent;
  }

  if (contactFormSuccess?.dataset.messageKey) {
    contactFormSuccess.textContent =
      dictionary[contactFormSuccess.dataset.messageKey] || contactFormSuccess.textContent;
  }

  if (contactFormSubmitButton?.dataset.state === "sending") {
    contactFormSubmitButton.textContent =
      dictionary["contact.formSending"] || contactFormSubmitButton.textContent;
  }

  storeLanguage(language);
};

const setContactFormFeedback = (type, messageKey = "") => {
  if (!contactFormError || !contactFormSuccess) return;

  [contactFormError, contactFormSuccess].forEach((element) => {
    element.hidden = true;
    element.textContent = "";
    delete element.dataset.messageKey;
  });

  if (!type || !messageKey) return;

  const target = type === "success" ? contactFormSuccess : contactFormError;
  const dictionary = getActiveDictionary();
  target.textContent = dictionary[messageKey] || "";
  target.dataset.messageKey = messageKey;
  target.hidden = false;
};

const setCurrentYear = () => {
  const year = String(new Date().getFullYear());
  currentYearNodes.forEach((node) => {
    node.textContent = year;
  });
};

const setFloatingTopButtonVisibility = (isVisible) => {
  if (!floatingTopButton) return;

  floatingTopButton.classList.toggle("is-visible", isVisible);
  floatingTopButton.setAttribute("aria-hidden", String(!isVisible));
  floatingTopButton.tabIndex = isVisible ? 0 : -1;
};

const updateFloatingTopButtonVisibility = () => {
  if (!floatingTopButton || !audienceSection) return;

  const headerHeight = siteHeader?.offsetHeight ?? 0;
  const threshold = Math.max(audienceSection.offsetTop - headerHeight - 24, 0);
  setFloatingTopButtonVisibility(window.scrollY >= threshold);
};

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.language);
  });
});

const initialLanguage = (() => {
  const savedLanguage = readStoredLanguage();
  return savedLanguage && translations[savedLanguage] ? savedLanguage : "pt";
})();

applyLanguage(initialLanguage);
setCurrentYear();
updateFloatingTopButtonVisibility();

window.addEventListener("scroll", updateFloatingTopButtonVisibility, { passive: true });
window.addEventListener("resize", updateFloatingTopButtonVisibility);

if (contactForm) {
  contactForm.addEventListener("submit", async (event) => {
    event.preventDefault();

    setContactFormFeedback();

    if (!contactForm.reportValidity()) return;

    const formData = new FormData(contactForm);
    const email = String(formData.get("email") || "").trim();
    const phone = String(formData.get("phone") || "").trim();

    if (!email && !phone) {
      setContactFormFeedback("error", "contact.formErrorRequiredContact");
      contactForm.querySelector('input[name="phone"]')?.focus();
      return;
    }

    const dictionary = getActiveDictionary();
    const payload = Object.fromEntries(formData.entries());
    payload.email = email;
    payload.phone = phone;
    payload.name = String(payload.name || "").trim();
    payload.message = String(payload.message || "").trim();
    payload.language = document.documentElement.lang;
    payload.page = contactForm.dataset.contactPage || "landing-page-contact-form";

    if (!payload.email) delete payload.email;
    if (!payload.phone) delete payload.phone;

    if (contactFormSubmitButton) {
      contactFormSubmitButton.disabled = true;
      contactFormSubmitButton.dataset.state = "sending";
      contactFormSubmitButton.textContent =
        dictionary["contact.formSending"] || contactFormSubmitButton.textContent;
    }

    try {
      const response = await fetch(contactForm.action, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Accept: "application/json",
        },
        body: JSON.stringify(payload),
      });

      if (!response.ok) {
        throw new Error(`Request failed with status ${response.status}`);
      }

      contactForm.reset();
      setContactFormFeedback("success", "contact.formSuccess");
    } catch {
      setContactFormFeedback("error", "contact.formErrorSend");
    } finally {
      if (contactFormSubmitButton) {
        contactFormSubmitButton.disabled = false;
        delete contactFormSubmitButton.dataset.state;
        contactFormSubmitButton.textContent =
          getActiveDictionary()["contact.formSubmit"] || contactFormSubmitButton.textContent;
      }
    }
  });
}

if ("IntersectionObserver" in window) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    {
      threshold: 0.15,
      rootMargin: "0px 0px -40px 0px",
    },
  );

  revealItems.forEach((item) => observer.observe(item));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}
